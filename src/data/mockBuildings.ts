// Helper functions
export const getBuildingById = (id: string): Building | undefined => {
  return mockBuildings.find(building => building.id === id);
};

export const getBuildingsByCategory = (category: string): Building[] => {
  if (category === 'all') return mockBuildings;
  return mockBuildings.filter(building => building.category === category || building.category === 'all');
};

export const getBuildingsByDistrict = (district: string): Building[] => {
  return mockBuildings.filter(building => building.district === district);
};

export const searchBuildings = (query: string): Building[] => {
  const lowerQuery = query.toLowerCase();
  return mockBuildings.filter(building => 
    building.name.toLowerCase().includes(lowerQuery) ||
    building.district.toLowerCase().includes(lowerQuery) ||
    building.address.toLowerCase().includes(lowerQuery) ||
    building.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};